// Helper functions for the expanded database
export function searchExpandedVideos(subject, classLevel, topic) {
  const subjectData = expandedVideoDatabase[subject.toLowerCase()];
  if (!subjectData) return [];

  // Determine class category
  let classCategory;
  const classNum = parseInt(classLevel.replace(/\D/g, ''));
  
  if (classNum <= 3) classCategory = 'primary';
  else if (classNum <= 6) classCategory = 'upperPrimary';
  else if (classNum <= 8) classCategory = 'middleSchool';
  else classCategory = 'highSchool';

  const categoryData = subjectData[classCategory];
  if (!categoryData) return [];

  // Search for topic matches
  const matchingVideos = [];
  for (const [topicKey, videos] of Object.entries(categoryData)) {
    if (topicKey.includes(topic.toLowerCase()) || topic.toLowerCase().includes(topicKey)) {
      matchingVideos.push(...videos);
    }
  }

  return matchingVideos;
}

export function getRandomExpandedVideo(subject, classLevel, topic) {
  const videos = searchExpandedVideos(subject, classLevel, topic);
  if (videos.length === 0) return null;
  
  return videos[Math.floor(Math.random() * videos.length)];
}

export function getAllExpandedVideosForSubject(subject, classLevel) {
  const subjectData = expandedVideoDatabase[subject.toLowerCase()];
  if (!subjectData) return [];

  const classNum = parseInt(classLevel.replace(/\D/g, ''));
  let classCategory;
  
  if (classNum <= 3) classCategory = 'primary';
  else if (classNum <= 6) classCategory = 'upperPrimary';
  else if (classNum <= 8) classCategory = 'middleSchool';
  else classCategory = 'highSchool';

  const categoryData = subjectData[classCategory];
  if (!categoryData) return [];

  const allVideos = [];
  for (const videos of Object.values(categoryData)) {
    allVideos.push(...videos);
  }

  return allVideos;
}

// Get total video count
export function getTotalVideoCount() {
  let total = 0;
  for (const subject of Object.values(expandedVideoDatabase)) {
    for (const classLevel of Object.values(subject)) {
      for (const topic of Object.values(classLevel)) {
        total += topic.length;
      }
    }
  }
  return total;
}
